from pulp import LpProblem, LpVariable, LpMaximize, LpStatus, value

# Створюємо задачу лінійного програмування для максимізації виробництва
model = LpProblem("Оптимізація виробництва", LpMaximize)

# Змінні: кількість вироблених Лимонаду (x1) і Фруктового соку (x2)
x1 = LpVariable("Лимонад", lowBound=0, cat="Continuous")
x2 = LpVariable("Фруктовий сік", lowBound=0, cat="Continuous")

# Цільова функція: максимізувати загальну кількість вироблених одиниць
model += x1 + x2, "Загальний обсяг виробництва"

# Обмеження ресурсів
# Вода: 2 * x1 (для лимонаду) + 1 * x2 (для фруктового соку) <= 100
model += 2 * x1 + 1 * x2 <= 100, "Обмеження води"

# Цукор: 1 * x1 <= 50
model += 1 * x1 <= 50, "Обмеження цукру"

# Лимонний сік: 1 * x1 <= 30
model += 1 * x1 <= 30, "Обмеження лимонного соку"

# Фруктове пюре: 2 * x2 <= 40
model += 2 * x2 <= 40, "Обмеження фруктового пюре"

# Розв'язання задачі
model.solve()

# Результати
results = {
    "Статус": LpStatus[model.status],
    "Лимонад": x1.varValue,
    "Фруктовий сік": x2.varValue,
    "Загальний обсяг виробництва": value(model.objective),
}

print(results)
